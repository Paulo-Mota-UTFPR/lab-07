  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_2);

  HAL_ADC_Start_DMA(&hadc1, adc_buffer, ADC_BUFFER_LENGTH);

  sprintf(string, "ADC with DMA Initialized. Waiting for commands...\r\n");
  CDC_Transmit_FS((uint8_t *)string, strlen(string));
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    if (gUSBRxBuffer[0] != 0) {
      switch (gUSBRxBuffer[0]) {
        case 'r': // Reset DMA buffer
          memset(adc_buffer, 0, sizeof(adc_buffer));
          sprintf(string, "ADC DMA buffer reset.\r\n");
          CDC_Transmit_FS((uint8_t *)string, strlen(string));
          break;

        default:
          sprintf(string, "Unknown command: %c\r\n", gUSBRxBuffer[0]);
          CDC_Transmit_FS((uint8_t *)string, strlen(string));
          break;
      }
      memset(gUSBRxBuffer, 0, sizeof(gUSBRxBuffer));
    }
  }
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
